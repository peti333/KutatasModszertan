# operátorok
a = 12
b = 20
c = "almafa"
l = [1, 2, 3, 4, 5]

# lista elemeinek összege
print(sum(l))

# lista hossza
print(len(c))

# string-é alakítás, majd szó hossza
print(len(str(a)))

# matekos operátorok
print(a + b)

print(a - b)

print(a * b)

print(a / b)

# osztásnak a maradéka
print(a % b)

# hatványozás, a a b-ediken
print(a ** b)

# összeadás string-en: összeköti a részszavakat
print("a" + "b")
print("a" + str(5))

# szorzás számmal
print("a" * 5)
